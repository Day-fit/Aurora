/**
 * Get template messages for a given language.
 * Falls back to English if the language is not supported.
 */
export function getTemplateMessages(language?: LanguageType): TemplateMessages {
  if (!language) {
    return messagesEn;
  }
  return messagesMap[language] || messagesEn;
}

